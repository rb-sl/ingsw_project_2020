package it.polimi.ingsw.Client.GUI;

/**
 * Class used to deserialize the location of the blocks images displayed to the right of the board
 */
public class Location {
    /**
     * Relative path to the block image
     */
    private String location;

    /**
     * Getter for the relative path of the block image
     * @return Path of the image
     */
    public String getLocation() {
        return location;
    }
}
